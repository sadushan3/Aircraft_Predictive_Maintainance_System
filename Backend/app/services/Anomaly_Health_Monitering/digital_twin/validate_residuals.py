"""
Residual Validation for CA-EDT-AHMA.

Validates residuals.csv generated by residual_calculator.py.

Checks:
1. Required files exist.
2. Row counts match:
   - scaled_features.csv
   - context_clusters.csv
   - ensemble_predictions.csv
   - residuals.csv
3. Required columns exist.
4. Row alignment is correct using unit_id, cycle, split.
5. Actual X_s values stored in residuals.csv match scaled_features.csv.
6. Ensemble prediction values stored in residuals.csv match ensemble_predictions.csv.
7. residual_Xs_* = actual_Xs_* - ensemble_predicted_Xs_*.
8. abs_residual_Xs_* = abs(residual_Xs_*).
9. Residual temporal features exist and are finite.
10. Optional exact temporal feature validation:
    - rolling mean
    - rolling std
    - trend
    - absolute residual rolling mean

Memory-safe:
- Reads all CSV files in chunks.
- Does not load full residuals.csv into RAM.
- Writes validation metrics safely.

Outputs:
- metrics/residual_validation_metrics.csv
- reports/residual_validation_summary.json
"""

